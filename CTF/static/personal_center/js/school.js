provinceOp = new provinceList();
schoolOp = new schoolList();

function delSchool(school)
{
	var len = school.length;
	for(i=0; i<len; i++)
	{
		school.remove(0);
		
	}
}

function addSchool(prov, school)
{
	var index = prov.selectedIndex;
	//alert(index);
	for(j=0; j<schoolOp[index].length; j++)
	{
		school.options.add(schoolOp[index][j]);
	}
}

function changeProvince(proObj, schoolObj)
{
	delSchool(schoolObj);
	addSchool(proObj,schoolObj);
	//alert("省份：" + proObj.value + "\t城市：" + cityObj.value);
}

function initialize(proObj, schoolObj, provinceID, schoolID)
{
	// 初始化省份列表
	for(i=0; i<provinceOp.length; i++)
	{
		proObj.options.add(provinceOp[i]);
	}
	for(j=0; j<proObj.options.length; j++)
	{
		if(proObj.options[j].value == provinceID)
		{
			proObj.options[j].selected = true;
		}
	}
	addSchool(proObj, schoolObj);
	for(k=0; k<schoolObj.options.length; k++)
	{
		if(schoolObj.options[k].value == schoolID)
		{
			schoolObj.options[k].value = true;
		}
	}
}